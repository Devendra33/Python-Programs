# program to convert hexa, octal, binary in int(decimal).

str=input("Enter in hexa:")
n=int(str,16)
print("hexa =",str,"decimal=",n)

str=input("Enter in octal:")
n=int(str,8)
print("octal =",str,"decimal=",n)

str=input("Enter in binary:")
n=int(str,2)
print("binary =",str,"decimal=",n)
